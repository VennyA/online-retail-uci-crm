WITH
	INVOICE_MONTH AS (
		SELECT
			CUSTOMERID,
			DATE_TRUNC('month', INVOICEDATE)::DATE AS INVOICE_MONTH
		FROM
			ONLINE_RETAILS
		GROUP BY
			CUSTOMERID,
			INVOICE_MONTH
	),
	FIRST_PURCHASE AS (
		SELECT
			CUSTOMERID,
			MIN(DATE_TRUNC('month', INVOICEDATE))::DATE AS FIRST_PURCHASE_MONTH
		FROM
			ONLINE_RETAILS
		GROUP BY
			CUSTOMERID
	),
	COHORT_INDEX AS (
		SELECT
			I.CUSTOMERID,
			INVOICE_MONTH,
			FIRST_PURCHASE_MONTH,
			(
				DATE_PART('year', AGE (INVOICE_MONTH, FIRST_PURCHASE_MONTH)) * 12 + DATE_PART(
					'month',
					AGE (INVOICE_MONTH, FIRST_PURCHASE_MONTH)
				)
			) AS COHORT_INDEX
		FROM
			INVOICE_MONTH I
			JOIN FIRST_PURCHASE F ON I.CUSTOMERID = F.CUSTOMERID
	),
	COHORT_COUNTS AS (
		SELECT
			FIRST_PURCHASE_MONTH,
			COHORT_INDEX,
			COUNT(DISTINCT CUSTOMERID) AS DISTINCT_CUSTOMERS
		FROM
			COHORT_INDEX
		GROUP BY
			FIRST_PURCHASE_MONTH,
			COHORT_INDEX
	),
	COHORT_SIZE AS (
		SELECT
			FIRST_PURCHASE_MONTH,
			COUNT(DISTINCT CUSTOMERID) COHORT_CUSTOMERS
		FROM
			COHORT_INDEX
		WHERE
			COHORT_INDEX = 0
		GROUP BY
			FIRST_PURCHASE_MONTH,
			COHORT_INDEX
	)
SELECT
	A.FIRST_PURCHASE_MONTH,
	COHORT_INDEX,
	COHORT_CUSTOMERS,
	ROUND(
		(DISTINCT_CUSTOMERS::DECIMAL / COHORT_CUSTOMERS) * 100,
		2
	) AS RETENTION_RATE
FROM
	COHORT_COUNTS A
	JOIN COHORT_SIZE B ON A.FIRST_PURCHASE_MONTH = B.FIRST_PURCHASE_MONTH;

WITH
	FREQUENCY AS (
		SELECT
			CUSTOMERID,
			COUNT(DISTINCT INVOICENO) AS FREQUENCY
		FROM
			ONLINE_RETAILS
		GROUP BY
			CUSTOMERID
	),
	MONETARY AS (
		SELECT
			CUSTOMERID,
			SUM(QUANTITY * UNITPRICE) AS MONETARY
		FROM
			ONLINE_RETAILS
		GROUP BY
			CUSTOMERID
	),
	AOV AS (
		SELECT
			F.CUSTOMERID,
			F.FREQUENCY,
			M.MONETARY,
			ROUND((M.MONETARY / F.FREQUENCY), 2) AS AOV
		FROM
			FREQUENCY F
			JOIN MONETARY M ON F.CUSTOMERID = M.CUSTOMERID
	),
	FIRST_PURCHASE AS (
		SELECT
			CUSTOMERID,
			MIN(DATE_TRUNC('month', INVOICEDATE))::DATE AS FIRST_PURCHASE_MONTH
		FROM
			ONLINE_RETAILS
		GROUP BY
			CUSTOMERID
	),
	LAST_PURCHASE AS (
		SELECT
			CUSTOMERID,
			MAX(DATE_TRUNC('month', INVOICEDATE))::DATE AS LAST_PURCHASE_MONTH
		FROM
			ONLINE_RETAILS
		GROUP BY
			CUSTOMERID
	),
	CUSTOMER_LIFESPAN AS (
		SELECT
			A.CUSTOMERID,
			FREQUENCY,
			MONETARY,
			A.AOV,
			DATE_PART(
				'year',
				AGE (LAST_PURCHASE_MONTH, FIRST_PURCHASE_MONTH)
			) * 12 + DATE_PART(
				'month',
				AGE (LAST_PURCHASE_MONTH, FIRST_PURCHASE_MONTH)
			) AS CUSTOMER_LIFESPAN
		FROM
			AOV A
			JOIN FIRST_PURCHASE FP ON A.CUSTOMERID = FP.CUSTOMERID
			JOIN LAST_PURCHASE LP ON LP.CUSTOMERID = A.CUSTOMERID
	)
SELECT
	CUSTOMERID,
	FREQUENCY,
	MONETARY,
	AOV,
	CUSTOMER_LIFESPAN,
	ROUND(
		(AOV * FREQUENCY * GREATEST(CUSTOMER_LIFESPAN, 1))::NUMERIC,
		2
	) AS CLV
FROM
	CUSTOMER_LIFESPAN
