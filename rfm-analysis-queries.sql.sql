WITH
	RECENCY AS (
		SELECT
			CUSTOMERID,
			'2011-12-10'::DATE - MAX(INVOICEDATE)::DATE AS RECENCY
		FROM
			ONLINE_RETAILS
		GROUP BY
			CUSTOMERID
	),
	FREQUENCY AS (
		SELECT
			CUSTOMERID,
			COUNT(DISTINCT INVOICENO) AS FREQUENCY
		FROM
			ONLINE_RETAILS
		GROUP BY
			CUSTOMERID
	),
	MONETARY AS (
		SELECT
			CUSTOMERID,
			SUM(QUANTITY * UNITPRICE) AS MONETARY
		FROM
			ONLINE_RETAILS
		GROUP BY
			CUSTOMERID
	),
	RFM_SCORING AS (
		SELECT
			R.CUSTOMERID,
			R.RECENCY,
			F.FREQUENCY,
			M.MONETARY,
			NTILE(4) OVER (
				ORDER BY
					RECENCY DESC
			) AS R_SCORE,
			NTILE(4) OVER (
				ORDER BY
					FREQUENCY
			) AS F_SCORE,
			NTILE(4) OVER (
				ORDER BY
					MONETARY
			) AS M_SCORE
		FROM
			RECENCY R
			JOIN FREQUENCY F ON R.CUSTOMERID = F.CUSTOMERID
			JOIN MONETARY M ON F.CUSTOMERID = M.CUSTOMERID
	),
	RFM_SEGMENTS AS (
		SELECT
			*,
			CASE
				WHEN R_SCORE = 4
				AND F_SCORE = 4
				AND M_SCORE = 4 THEN 'champions'
				WHEN R_SCORE >= 3
				AND F_SCORE >= 3 THEN 'loyal_Customers'
				WHEN R_SCORE = 1
				AND F_SCORE = 1 THEN 'Lost'
				WHEN R_SCORE <= 2
				AND F_SCORE <= 2 THEN 'hibernating'
				WHEN R_SCORE >= 2
				AND F_SCORE >= 2 THEN 'at_risk'
				WHEN R_SCORE <= 2
				AND F_SCORE >= 3 THEN 'cannot_lose_them'
				WHEN R_SCORE >= 4
				AND F_SCORE <= 2 THEN 'new_customers'
				WHEN R_SCORE = 3
				AND F_SCORE <= 2 THEN 'need_attention'
				ELSE 'others'
			END AS SEGMENTS
		FROM
			RFM_SCORING
	),
	CHURN_RISK AS (
		SELECT
			*,
			CASE
				WHEN RECENCY >= 180 THEN 'At_risk_of_churn'
				ELSE 'Active'
			END AS CHURN_RISKS
		FROM
			RFM_SEGMENTS
	),
	CHURN AS (
		SELECT
			*,
			CASE
				WHEN CHURN_RISKS = 'At_risk_of_churn' THEN 1
				ELSE 0
			END AS CHURN_FLAG
		FROM
			CHURN_RISK
	)
SELECT
	*
FROM
	CHURN;

